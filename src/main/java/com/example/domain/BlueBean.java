public class BlueBean {
    private String blue;
    private String count;

    public BlueBean(String blue, String count) {
        this.blue = blue;
        this.count = count;
    }

    public String getBlue() {
        return blue;
    }

    public void setBlue(String blue) {
        this.blue = blue;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }
}
